-- Create a function to check if a value is NaN
CREATE OR REPLACE FUNCTION is_nan(f FLOAT)
RETURNS BOOLEAN AS $$
BEGIN
    RETURN f != f;
END;
$$ LANGUAGE plpgsql IMMUTABLE;

